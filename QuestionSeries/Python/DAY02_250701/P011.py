# for문
# for [변수] in [범위]

# 1) str
str = "notone"
for s in str:
    print(s)
print("*****")

# 2) list
scope = [1,2,3,4,5]
for x in scope:
    print(x)
print("*****")

# 3) dictionary ***
dict = {'a':99,'b':90}
for d in dict:
    print(d)
print("*****")

# 4) range
for c in range(10):
    print(c)
print("*****")